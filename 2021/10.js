const solve = (input) => {

    const lines = input.split("\n");

    const tokenPairs = {"(": ")", "[": "]", "{": "}", "<": ">"};
    const errorTokenValues = {")": 3, "]": 57, "}": 1197, ">": 25137};
    const completionTokenValues = {")": 1, "]": 2, "}": 3, ">": 4};

    const processTokens = (tokens) => {
        const stack = [];
        while (tokens.length > 0) {
            const token = tokens[0];
            if (token in tokenPairs) {
                stack.push(token);
            } else {
                const top = stack.pop();
                if (token !== tokenPairs[top]) {
                    break;
                }
            }
            tokens.shift();
        };
        return {tokens, stack};
    }

    const getErrorScore = (tokens) => {
        const errorToken = tokens[0];
        return errorTokenValues[errorToken];
    }

    const getCompletionScore = (stack) => {
        const completionTokens = stack.reverse().map(t => tokenPairs[t]);
        return completionTokens.reduce((sum, t) => sum * 5 + completionTokenValues[t], 0);
    }

    const errorScores = [];
    const completionScores = [];

    lines.forEach(line => {
        const {tokens, stack} = processTokens(line.split(""));
        if (tokens.length > 0) {
            errorScores.push(getErrorScore(tokens));
        } else {
            completionScores.push(getCompletionScore(stack));
            return 0;
        }
    });

    const result1 = errorScores.reduce((total, score) => total + score, 0);

    const result2 = completionScores.sort((a, b) => a - b)[(completionScores.length - 1) / 2];

    return [result1, result2];
}

