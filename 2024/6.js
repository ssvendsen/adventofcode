const solve = (input) => {

    const lines = input.split("\n");

    const originalMap = lines.map(line => line.split(""));
    const mapHeight = originalMap.length;
    const mapWidth = originalMap[0].length;

    const atPos = (map, pos) => {
        const [x, y] = pos;
        return map[y][x];
    }

    const posOf = (map, c) => {
        for (let x = 0; x < mapWidth; x++) {
            for (let y = 0; y < mapHeight; y++) {
                const pos = [x, y];
                if (atPos(map, pos) === c) {
                    return pos;
                }
            }
        }
    }

    const isOffMap = (pos) => {
        const [x, y] = pos;
        return x < 0 || x >= mapWidth || y < 0 || y >= mapWidth;
    }

    // Directions 0 = up, 1 = right, 2 = down, 3 = left
    const vectors = [[0, -1], [1, 0], [0, 1], [-1, 0]];
    const turn = (dir) => (dir + 1) % 4;
    const advance = (pos, dir) => {
        const [x0, y0] = pos;
        const [dx, dy] = vectors[dir];
        return [x0 + dx, y0 + dy];
    }

    const run = (map, pos, dir) => {

        const visited = new Set();
        const trail = new Set();

        while (true) {
            const nextPos = advance(pos, dir);
            if (isOffMap(nextPos)) {
                break;
            }
            if (atPos(map, nextPos) === '#') {
                dir = turn(dir);
            } else {
                pos = nextPos;
                visited.add(pos.join());

                const trailKey = pos.join() + '-' + dir;
                if(trail.has(trailKey)) {
                    return undefined;
                }
                trail.add(trailKey);
            }
        }

        return visited;
    }

    const startPos = posOf(originalMap, '^');
    const startDir = 0;

    const visited = run(originalMap, startPos, startDir);

    const obstacles = new Set();
    visited.forEach(step => {
        const obstaclePos = step.split(',').map(n => +n);
        const [x, y] = obstaclePos;

        const modifiedMap = originalMap.map(row => [...row]);
        modifiedMap[y][x] = '#';

        if (!run(modifiedMap, startPos, startDir)) {
            obstacles.add(obstaclePos.join());
        }

    })

    const result1 = visited.size;
    const result2 = obstacles.size;

    return [result1, result2];
}

